;mov bx, 101h    ; VBE mode 101h
int 10h

GameLoop:

mov al, 0FFh
call clearScreen
;call printMaze
mov di, [xPos]
mov si, sprite
call printGhost
mov bx, [xPos]
add bx, [xVelocity]
